function isLegacySportingDirectorAvatarKey(
  value: string | null | undefined
): value is LegacySportingDirectorAvatarKey {
  return SPORTING_DIRECTOR_AVATARS.some((avatar) => avatar.key === value);
}

function randomKey<T extends readonly { key: string }[]>(
  options: T,
  random: () => number
): T[number]["key"] {
  const index = Math.min(
    options.length - 1,
    Math.max(0, Math.floor(random() * options.length))
  );

  return options[index].key;
}
