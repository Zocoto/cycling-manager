export function encodeSportingDirectorAvatar(
  config: SportingDirectorAvatarConfig
): string {
  return `${CUSTOM_AVATAR_PREFIX}${avatarConfigFields
    .map((field) => config[field])
    .join(".")}`;
}

export function decodeCustomSportingDirectorAvatar(
  value: string | null | undefined
): SportingDirectorAvatarConfig | null {
  if (!value?.startsWith(CUSTOM_AVATAR_PREFIX) || value.length > 512) {
    return null;
  }

  const parts = value.slice(CUSTOM_AVATAR_PREFIX.length).split(".");

  if (parts.length !== avatarConfigFields.length) {
    return null;
  }

  const config = {} as Record<keyof SportingDirectorAvatarConfig, string>;

  for (const [index, field] of avatarConfigFields.entries()) {
    const part = parts[index];

    if (!part || !optionSets[field].has(part)) {
      return null;
    }

    config[field] = part;
  }

  return config as SportingDirectorAvatarConfig;
}

export function resolveSportingDirectorAvatar(
  value: string | null | undefined
): SportingDirectorAvatarConfig {
  const customConfig = decodeCustomSportingDirectorAvatar(value);

  if (customConfig) {
    return customConfig;
  }

  if (isLegacySportingDirectorAvatarKey(value)) {
    return legacyAvatarConfigs[value];
  }

  return DEFAULT_SPORTING_DIRECTOR_AVATAR;
}

export function isSportingDirectorAvatarKey(
  value: string | null | undefined
): value is string {
  return Boolean(
    isLegacySportingDirectorAvatarKey(value) ||
      decodeCustomSportingDirectorAvatar(value)
  );
}

